			<h2> Déficit, manutenção e superávit calórico simplificados </h2>

			<p>	

			&nbsp &nbsp Imagine que você recebe R$3000 por semana e seus custos totalizam R$2000. No final do mês, você terá R$1000 de reserva.	Se você passasse a gastar R$3500, você teria uma dívida de R$500 a cada mês, certo?	A reserva neste exemplo, faz analogia à sua <b> massa gorda </b>: sua reserva de gordura. Para uma pessoa emagrecer, ela deve consumir menos caloria do que ela necessita, para queimar a massa gorda. Agora, usando termos de nutrição fica mais fácil entender, vejamos: supondo que um indivíduo necessite de <b>2000kcal por dia</b>. Caso esse indivíduo consuma <b>2500kcal por dia </b>, ele irá ganhar massa gorda, pois estará consumindo mais do que está gastando. Se ele consumir <b>1500kcal por dia</b>, estará perdendo massa gorda, ou seja, <b>emagrecendo</b>. 

			</p>

