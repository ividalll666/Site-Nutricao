			<h2> Conclusão: </h2>

			<p>

			<p> 

			&nbsp "Hm, então indivíduo que necessita de 2000kcal diárias, consumir 2500kcal, engordará para sempre?"

			</p> 

			<p>

			&nbsp &nbsp Bom, a resposta é não. Mas por que não? Simples, porque a necessidade calórica está ligada ao peso. Quanto mais peso, mais calorias são necessárias para manter o peso. É por esse motivo que pessoas altas geralmente comem mais, pois elas tem mais massa. Aqui podemos observar uma das fórmulas utilizadas para calcular a taxa metabólica basal de indivíduos do sexo masculino.

			</p>

			<div class="tabela">

			<p id="tbm"> TMB </p> 

			<p> 66,5 + ( 13,75. P ) + ( 5,003. A ) - ( 6,755. I ). </p>

			<p>	P = Peso em Kg; </p> 
			<p> A = Altura em cm; </p>
			<p> I = idade em anos </p>

			</div>


			<p>

			&nbsp &nbsp Ao olharmos a fórmula, podemos concluir que quanto mais peso e altura a pessoa tem, maior será a <i>TMB</i> dela. Importante destacar que esse é apenas um dos fatores utilizados para calcular seu gasto energético total, então peço que não se baseiem apenas nisso para montar uma dieta. O correto é que o profissional nutricionista avalie o que é necessário e monte a dieta se adequando aos seus dados.

