			</p>

			<h2> O que é emagrecer? </h2>

			<p>

			&nbsp &nbsp O emagrecimento não está relacionado ao seu peso corporal, e sim a sua <i> massa gorda </i>, ou em termos mais simples, a gordura que está estocada no seu corpo. Emagrecer é diminuir a quantidade de massa gorda que seu corpo possui. O ato de se pesar depois de um treino intenso e relatar um peso menor do que antes é muito comum entre os praticantes de atividade física, entretanto, lamento informar que aquele peso menor não significa que você perdeu tudo aquilo de gordura. É claro que um pouco de gordura foi queimada, mas um quantidade ínfima. A maior parte do peso perdido após um treino intenso é devido a perda de água no suor, com a função de equilibrar a sua temperatura corporal durante e após o treino. Além disso, é importante ressaltar que não importa o quanto você treine para emagrecer, se sua dieta não estiver com os valores adequados, você não irá ter o resultado esperado. 

			</p>

			
